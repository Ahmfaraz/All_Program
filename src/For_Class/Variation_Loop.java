
package For_Class;

import java.util.Scanner;

public class Variation_Loop
{
    public static void main(String[] args)
    {
        int i,j;
        for(i=1,j=5;i<5;i++,j--)
        {
            System.out.println("The value of i " + i + " The value of j " +j);
        }
    }
   
}
